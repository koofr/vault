use rand_core::{OsRng, RngCore};

use super::{constants::FILE_NONCE_SIZE, CipherError};

#[derive(Clone, PartialEq, Eq, Debug, Default)]
pub struct Nonce {
    value: [u8; FILE_NONCE_SIZE],
}

/// Based on rclone implementation
/// https://github.com/rclone/rclone/blob/7be9855a706d1e09504f17949a90c54cd56fb2a5/backend/crypt/cipher.go#L601
impl Nonce {
    pub fn new(nonce: &[u8; FILE_NONCE_SIZE]) -> Self {
        Self {
            value: nonce.clone(),
        }
    }

    pub fn new_random() -> Result<Self, CipherError> {
        let mut nonce: [u8; FILE_NONCE_SIZE] = [0; FILE_NONCE_SIZE];

        (&mut OsRng)
            .try_fill_bytes(&mut nonce)
            .map_err(CipherError::from)?;

        Ok(Self { value: nonce })
    }

    /// Carry 1 up the nonce from position i
    fn carry(&mut self, i: usize) {
        for i in i..FILE_NONCE_SIZE {
            let digit = self.value[i];
            if digit == 255 {
                self.value[i] = 0;
            } else {
                self.value[i] = digit + 1;
                break;
            }
        }
    }

    pub fn increment(&mut self) {
        self.carry(0)
    }

    // add a u64 to the nonce
    pub fn add(&mut self, x: u64) {
        let mut x = x;
        let mut carry: u16 = 0;
        for i in 0..8 {
            let digit = self.value[i];
            let x_digit = x as u8;
            x = x >> 8;
            carry += digit as u16 + x_digit as u16;
            self.value[i] = carry as u8;
            carry = carry >> 8;
        }
        if carry != 0 {
            self.carry(8);
        }
    }

    pub fn as_slice(&self) -> &[u8] {
        &self.value
    }

    pub fn as_nonce(&self) -> &xsalsa20poly1305::Nonce {
        (&self.value).into()
    }
}

